"""rules of Variables in python
1. A variable name must start with a letter or an underscore (_).
2. A variable name cannot start with a number.
3. A variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _).
4. Variable names are case-sensitive (age, Age and AGE are three different variables).
5. Variable names cannot be a reserved keyword in Python (e.g., if, else, while, for, etc.).
6. Variable names should be descriptive and meaningful to improve code readability.
7. Variable names cannot contain spaces. Instead, you can use underscores to separate words (e.g., my_variable).
"""


name = "shakil"
age = 22
is_student = True

print("Name:", name)
print("Age:", age)
print("Student:", is_student)
