"""data types in python are used to store and manipulate data. There are several built-in data types in python, including:
1. Numeric Types: int, float, complex
2. Sequence Types: list, tuple, range
3. Text Type: str
4. Mapping Type: dict"""

a = 10
b = 3.14
c = 2 + 3j
print("Integer:", type(a))
print("Float:", type(b))
print("Complex:", type(c))



is_student = True
print("Boolean:", type(is_student))  # <class 'bool'>

name = "Alice"
print("String:", type(name))  # <class 'str'>

my_list = [1, 2, 3, 4, 5]
print("List:", type(my_list))  # <class 'list'>

my_tuple = (1, 2, 3, 4, 5)
print("Tuple:", type(my_tuple))  # <class 'tuple'>

my_range = range(1, 10)
print("Range:", type(my_range))  # <class 'range'>

my_dict = {"name": "Alice", "age": 30, "city": "New York"}
print("Dictionary:", type(my_dict))  # <class 'dict'>




