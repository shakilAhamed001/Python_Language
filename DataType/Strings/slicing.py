"""slicing is a powerful feature in Python that allows you to extract a portion of a string, list, or other sequence types. The syntax for slicing is as follows:
sequence[start:stop:step]

"""

name = "Hello, World!"
# Slicing the string to get "Hello"
print(name[0:5])  # Output: Hello
# Slicing the string to get "World"
print(name[7:12])  # Output: World


# Slicing the string to get "Hlo ol!"
print(name[0:13:2])  # Output: Hlo ol!

