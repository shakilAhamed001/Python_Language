#assignment operator
#Assignment operators are used to assign values to variables. They include the basic assignment operator (=) and compound assignment operators (+=, -=, *=, /=, %=, **=, //=).
x = 5
print("Initial value of x:", x)  # Output: 5    


#compound assignment operators
x += 3  # Equivalent to x = x + 3
print("After addition (x += 3):", x)  # Output: 8

x -= 2  # Equivalent to x = x - 2
print("After subtraction (x -= 2):", x)  # Output: 6

x *= 4  # Equivalent to x = x * 4
print("After multiplication (x *= 4):", x)  # Output: 24

x /= 6  # Equivalent to x = x / 6
print("After division (x /= 6):", x)  # Output: 4

x %= 3  # Equivalent to x = x % 3

print("After modulus (x %= 3):", x)  # Output: 1

x **= 2  # Equivalent to x = x ** 2
print("After exponentiation (x **= 2):", x)  # Output: 1

x //= 2  # Equivalent to x = x // 2
print("After floor division (x //= 2):", x)  # Output: 0

