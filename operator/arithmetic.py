"""operator are special symbols that perform specific operations on one or more operands. They are used to manipulate data and variables in Python. 
Here are some common types of operators in Python:  
Arithmetic Operators: +, -, *, /, %, **, //"""

a = 10
b = 3       
print("Addition:", a + b)         # Output: 13
print("Subtraction:", a - b)      # Output: 7   
print("Multiplication:", a * b)   # Output: 30
print("Division:", a / b)         # Output: 3.333333333333
print("Modulus:", a % b)          # Output: 1
print("Exponentiation:", a ** b)   # Output: 1000
print("Floor Division:", a // b)  # Output: 3


#BODMAS 
#BODMAS stands for Brackets, Orders (i.e., powers and square roots, etc.), Division and Multiplication, Addition and Subtraction.


result = 10 + 5 * 2 - (8 / 4) ** 2
print("Result:", result)  # Output: 17.0
