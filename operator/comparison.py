"""comparison operators are used to compare two values and return a boolean result (True or False). They include the following operators:  
Equal to (==): Returns True if the values on either side are equal.
Not equal to (!=): Returns True if the values on either side are not equal. 
Greater than (>): Returns True if the value on the left is greater than the value on the right.
Less than (<): Returns True if the value on the left is less than the value on the right."""    
a = 10
b = 5
print("Equal to (a == b):", a == b)           # Output: False
print("Not equal to (a != b):", a != b)       # Output: True
print("Greater than (a > b):", a > b)         # Output: True
print("Less than (a < b):", a < b)            # Output: False
print("Greater than or equal to (a >= b):", a >= b)  # Output: True
print("Less than or equal to (a <= b):", a <= b)     # Output: False
